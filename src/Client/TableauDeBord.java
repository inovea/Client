    private void createAllJTable() throws Exception {

        if (utilisateurs == null) {
            utilisateurs = new JTableModelUtilisateur();
        }
        jTableCourier.setModel(utilisateurs);
        TableRowSorter<TableModel> sorterCourier = new TableRowSorter<>(jTableCourier.getModel());
        jTableCourier.setRowSorter(sorterCourier);

        Selected utilisateur = new Selected(jTableCourier, jLabelUtilisateurSelected);
        utilisateur.start();

        if (containerModel == null) {
            containerModel = new JTableModelContainer();
        }
        jTableContainer.setModel(containerModel);
        TableRowSorter<TableModel> sorterContainer = new TableRowSorter<>(jTableContainer.getModel());
        jTableContainer.setRowSorter(sorterContainer);

        Selected container = new Selected(jTableContainer, jLabelContainerSelected);
        container.start();

        if (errands == null) {
            errands = new JTableModelErrand();
        }
        jTableErrand.setModel(errands);
        TableRowSorter<TableModel> sorterErrand = new TableRowSorter<>(jTableErrand.getModel());
        jTableErrand.setRowSorter(sorterErrand);

        Selected errand = new Selected(jTableErrand, jLabelErrandSelected);
        errand.start();
        
        if(alerts == null)
            alerts = new JTableModelAlert();
        jTableAlert.setModel(alerts);
        TableRowSorter<TableModel> sorterAlert = new TableRowSorter<>(jTableAlert.getModel());
        jTableAlert.setRowSorter(sorterAlert);

        Selected alert = new Selected(jTableAlert, jLabelAlertSelected);
        alert.start();
    }
     private void fireJTableAlert(List<Alert> list, Alert alert) {
        if (list != null) {
            alerts = new JTableModelAlert(list);
            jTableAlert.setModel(alerts);
            TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTableAlert.getModel());
            jTableAlert.setRowSorter(sorter);
        } else if (alert != null) {
            alerts = new JTableModelAlert(alert);
            jTableAlert.setModel(alerts);
            TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTableAlert.getModel());
            jTableAlert.setRowSorter(sorter);
        } else {
            try {
                alerts = new JTableModelAlert();
                jTableAlert.setModel(alerts);
                TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTableAlert.getModel());
                jTableAlert.setRowSorter(sorter);
            } catch (Exception ex) {
                Logger.getLogger(TableauDeBord.class.getName()).log(Level.SEVERE, null, ex);
                jLabelNbFoundAlert.setText("<html><body><font color='red'>" + ex.getMessage() + "</font></body></html>");
            }
        }
    }

