    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
         try {
            cont.setName(tbNom.getText());
            cont.setAddress(tbAdd.getText());
            cont.setState(cbEditEtat.getSelectedIndex());     
            cont.setLat(Double.parseDouble(lbEditLatitude.getText()));
            cont.setLng(Double.parseDouble(lbEditLng.getText()));
            containerMetierService.update(cont);
        } catch (Exception ex) {
            Logger.getLogger(AddContainer.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            tb.fireJTableContainer(this.containerMetierService.getAll(), null);
        } catch (Exception ex) {
            Logger.getLogger(AddContainer.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_jButtonEditActionPerformed

